import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { glass?: boolean }
>(({ className, glass = true, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-2xl border transition-all duration-300",
      glass
        ? "bg-white border-slate-300 shadow-lg shadow-slate-200/50 dark:bg-zinc-900 dark:border-zinc-800 dark:shadow-xl dark:shadow-black/60 backdrop-blur-xl text-slate-900 dark:text-white"
        : "bg-white border-slate-300 text-slate-900 dark:bg-zinc-900 dark:border-zinc-800 dark:text-white",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

